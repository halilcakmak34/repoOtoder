    public int checkEntry( String name , String password ) throws SQLException{
        DBHelper dbHelper = new DBHelper("otoder");
        ResultSet rs = dbHelper.recordSelect("yonetici", "*", "", "");
        String m_name,m_pass;

        while( rs.next() ){
            m_name = rs.getString( rs.findColumn("isim"));
            m_pass = rs.getString( rs.findColumn("sifre"));
            if( name.equals(name) && m_pass.equals(password)){
                return rs.getInt(rs.findColumn("id"));
            }
        }
        infoJLabel.setText("Kullanici Bulunamadi!");
        return 0; 
    }
    
