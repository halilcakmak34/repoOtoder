    private void buttonGirisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonGirisMouseClicked
        int admin_id = 0;
        try {
            String name = kullaniciAdiTextField.getText().toString();
            String password = sifreTextField.getText().toString();
            
            if( name.length() == 0 || password.length() == 0 ){
                infoJLabel.setText("Lutfen bos alanlari giriniz !");
                return;
            }
            admin_id = checkEntry(name, password);
            System.out.println("Entry : " + admin_id );
            
        } catch (SQLException ex) {
            Logger.getLogger(SistemGirisPaneli.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        if( admin_id > 0 ){
            DershanePaneli dershane = new DershanePaneli( admin_id, this );
            this.setVisible(false);
            dershane.setVisible(true);
        }
        
    }//GEN-LAST:event_buttonGirisMouseClicked

