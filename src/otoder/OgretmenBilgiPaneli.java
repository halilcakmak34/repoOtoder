    private void ogretmentKaydet() {
        try {
            DBHelper dbHelper = new DBHelper("otoder");
            ResultSet rs = dbHelper.recordSelect("ogretmen", "*", "", "");
            
            while(rs.next()){
                if(adTxtFld.getText().equals(rs.getString( rs.findColumn("isim"))) &&
                       soyadTxtFld.getText().equals(rs.getString( rs.findColumn("soyisim"))) && 
                        bransTxtFld.getText().equals(rs.getString( rs.findColumn("brans")))){
                    BilgilendirmePaneli bilgiEkran=new BilgilendirmePaneli( this , "isim   soyad   brans\n" +
                           adTxtFld.getText().toString()+soyadTxtFld.getText().toString()+bransTxtFld.getText().toString() );
                   // JPanel bilgiEkran=new JPanel();
                   // bilgiEkran.setSize(200, WIDTH);
                    bilgiEkran.setVisible(true);
                    
                    
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
